package baekjoon.level_bronze;

import java.util.Scanner;

public class BOJ2742_기찍N {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        for (int i = n; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
